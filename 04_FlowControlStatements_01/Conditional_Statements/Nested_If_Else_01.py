num = int(input("Enter any positive or negative number - "))

if num > 0 :
    print("Positive Number")
    if num % 2 == 0 :
        print("Positive even number")
    else :
        print("Positive odd number")
elif num < 0 :
    print("Negative Number")
    if num % 2 == 0 :
        print("Negative even number")
    else :
        print("Negative odd number")










